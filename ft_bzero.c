#include "libft.h"

void	*ft_bzero(void *dest, size_t n)
{
	size_t			i;
	unsigned char	*s;

	s = dest;
	i = 0;
	while (i != n)
	{
		s[i] = '\0';
		i++;
	}
	return (s = dest);
}
